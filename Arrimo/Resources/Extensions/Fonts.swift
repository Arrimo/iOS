import Foundation
import UIKit

extension UIFont {
    
    static func kufam(size: CGFloat) -> UIFont? {
        return UIFont(name: "Kufam", size: size)
    }
    
    static func kufamSemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Kufam-SemiBold", size: size)
    }
    
    static func kufamMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Kufam-Medium", size: size)
    }
    
    static func kufamBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Kufam-Bold", size: size)
    }
    
    static func tektur(size: CGFloat) -> UIFont? {
        return UIFont(name: "Tektur", size: size)
    }
    
    static func tekturSemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Tektur-SemiBold", size: size)
    }
    
    static func tekturMedium(size: CGFloat) -> UIFont? {
        return UIFont(name: "Tektur-Medium", size: size)
    }
    
    static func tekturBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Tektur-Bold", size: size)
    }
    
}
